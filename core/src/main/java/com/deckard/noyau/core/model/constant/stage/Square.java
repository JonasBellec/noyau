package com.deckard.noyau.core.model.constant.stage;

public class Square {

	private Integer squareType;
	private Integer x;
	private Integer y;

	public Integer getSquareType() {
		return squareType;
	}

	public void setSquareType(Integer squareType) {
		this.squareType = squareType;
	}

	/**
	 * @return the x
	 */
	public Integer getX() {
		return x;
	}

	/**
	 * @param x
	 *            the x to set
	 */
	public void setX(Integer x) {
		this.x = x;
	}

	/**
	 * @return the y
	 */
	public Integer getY() {
		return y;
	}

	/**
	 * @param y
	 *            the y to set
	 */
	public void setY(Integer y) {
		this.y = y;
	}
}
